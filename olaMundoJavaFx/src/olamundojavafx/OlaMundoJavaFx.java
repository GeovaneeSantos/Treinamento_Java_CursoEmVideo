package olamundojavafx;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;
// Iporta as bibliotecas javafx para usar recursos de interface
/**
 *
 * @author Geovane
 */
public class OlaMundoJavaFx extends Application {
// usa o paramatro extends (recurso de POO)que faz "OlaMundoJavaFx" herdar tudo que a aplicação possui
    @Override
    public void start(Stage stage) throws Exception {
        Parent root = FXMLLoader.load(getClass().getResource("FXMLDocument.fxml"));
        
        Scene scene = new Scene(root);
        
        stage.setScene(scene);
        stage.show();
    }

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        launch(args);
    }
    
}
